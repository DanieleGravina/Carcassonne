package common;

import java.io.Serializable;

/**
 * E' L'evento generato in seguito al posizionamento di una tessera
 * @author Daniele Iamartino, Daniele Gravina
 *
 */
public class UpdateTileEvent implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 3186565658588022710L;
	private final CoordinatesTile source;

	public UpdateTileEvent(CoordinatesTile source){
		this.source = source;
	}
	
	public CoordinatesTile getSource(){
		return source;
	}


}

package common;


/**
 * Indica che la costruzione scelta non e' valida o non puo' essere utilizzata in un determinato contesto.
 * 
 * @author Daniele Iamartino, Daniele Gravina
 */
public class InvalidConstructionException extends Exception{
	/**
	 * 
	 */
	private static final long serialVersionUID = -4684472800187488496L;

	public InvalidConstructionException(){
		super();
	}
	
	public String toString(){
		return "Invalid construction";
	}
}
